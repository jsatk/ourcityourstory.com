                <div class="column clearfix center cushycms" title="Feature episode description"><p>
	Epictetus &ldquo;He is a wise man who does not grieve for the things which he has not, but rejoices for those which he has.&rdquo; We live in a world amply sprinkled with naysayers. Hearing the word NO more than YES has become the rule, not the exception. No, you&rsquo;re not smart. No, you&rsquo;re not accepted. No, you can&rsquo;t do it... But bearing the brunt of lifelong denial to one&rsquo;s independence is inconceivable for the average person. This is why I&rsquo;m incredibly humbled and honored today to write about a man I know.</p>
<p>
	When I quietly pass by him daily, I always see him smiling&hellip;every single time, and I&rsquo;ve rarely seen a person enjoy their work more. He is the supervisor of the janitorial team at Barbara Olson Center of Hope and, apart from being sick at times, he has never missed a day in his working years. I remember he recounted to me, &ldquo;It was Christmas one time. I had nothing&hellip;I thought to myself, I will never be in a situation like this again.&rdquo;</p>
<p>
	Today, he is a rich man. Wealthy in mind and heart because he has populated within his spirit some of the greatest virtues we strive for - courage, tenacity, unbreakable willpower, patience, and most importantly, gratitude. Being present and aware enough to realize the power of gratitude and contentment for what we already have is something we shamefully take for granted. Though simple and seemingly unimportant, when mastered, this realization holds the key to happiness. And isn&rsquo;t achieving happiness the ultimate goal of our existence? This man - 51 year old Darrell Hines, a Rockford native - is a happy man.</p>
<p>
	How did he become this man despite the fact that he is physically disabled, developmentally disabled with a diagnosis of severe cerebral palsy, uses a wheelchair, and has fairly good use of only one arm? Well, he said NO to the naysayers. It took him the first 25 years of his life to be able to stand up for himself and fight for his independence even though he was repeatedly told he couldn&rsquo;t take care of himself. He begged his doctor to let him out of the nursing home and give him a chance to make it on his own. He was given that chance and he hasn&rsquo;t looked back since. Fighting through the physical ailments and pain brought on by his condition, he prides himself on earning and working hard for everything he has. And as he should. Darrell advocated for himself and demanded to be a productive citizen. He did this by fighting to get himself a working wheelchair when he was denied replacement to one that was nearly falling apart. These are my &ldquo;legs&rdquo; he says...&ldquo;I need them to work.&rdquo; Now he oversees about 10 people in his department, who are also disabled, and not only supervises them but also teaches them to carry on his lessons of work ethic, striving to do the best one can, and beating the odds.</p>
<p>
	A respected sage and philosopher of his time in ancient Greece, Epictetus is depicted in historical art as a &ldquo;crippled&rdquo; old man accompanied by a crutch. Born and bred as a slave, he acquired permission from his owner to pursue his passion for studying philosophy. He eventually got his freedom and accumulated respect through education and his teachings. Epictetus held fast that we only become enslaved by desiring that which is beyond our grasp and trying to control the uncontrollable fate. If we let go of this, happiness as well as peace of mind would follow.</p>
<p>
	What was true in 100AD, remains true now in 2012.</p>
