                <div title="Feature episode description" class="column clearfix center cushycms"><p>
	Can one man change the world? Yes&mdash;pre-eminent historian James Henry Breasted did just that, simply by changing the way we look at the world and its history.</p>
<p>
	Elementary school students first learn of the Fertile Crescent in world geography lessons. World leaders understand the power and importance of this cradle of civilization, as they continually negotiate the territory&rsquo;s political climate. But before Breasted coined the term and conveyed its significance, the Fertile Crescent&rsquo;s role in the development of western civilization had been ignored. By enlarging the world&rsquo;s perspective from its formerly Eurocentric vision, Breasted opened the doors to further discovery and understanding.</p>
<p>
	How did Breasted, born in a small Midwestern city to a small hardware business owner, end up traveling the world and uncovering its archaeological truths? By following his bliss. By knowing and seeking his true passion and persevering in its pursuit.</p>
<p>
	Breasted&rsquo;s story needed an equal voice to tell it and local artist Jim Julin&rsquo;s bold timbre provided that perfect counterpart. With his commanding, larger-than-life presence, Julin has been a de facto pioneer, an artist revered by the community at large. Like Breasted, Julin followed his bliss, and in doing so, created a better world in his own city. He also seemed to inherently understand Breasted&rsquo;s vision that only by seeing our true past and revealing it in a different light, can we create a more vital and perfect present.</p>
<p>
	When one follows their bliss, one can indeed change the world.</p>
<p>
	&lsquo;Follow your bliss.&rsquo; Find where it is, and don&rsquo;t be afraid to follow it. &mdash; Joseph Campbell</p>
